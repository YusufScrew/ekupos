    public function data()
    {
        $user = User::isNotAdmin()->orderBy('id', 'desc')->get();

        return datatables()
            ->of($user)
            ->addIndexColumn()
            ->addColumn('aksi', function ($user) {
                return '
                <div class="btn-group">
                    <button type="button" onclick="editForm(`'. route('user.update', $user->id) .'`)" class="btn btn-sm btn-icon me-2 btn-warning"><span class="tf-icons bx bx-edit"></span></button>
                    <button type="button" onclick="deleteData(`'. route('user.destroy', $user->id) .'`)" class="btn btn-sm btn-icon me-2 btn-danger"><span class="tf-icons bx bx-trash"></span></button>
                </div>
                ';
            })
            ->rawColumns(['aksi'])
            ->make(true);
    }

